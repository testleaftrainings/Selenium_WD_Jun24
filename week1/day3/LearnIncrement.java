package week1.day3;

public class LearnIncrement {

	public static void main(String[] args) {
		int i = 0;
		
		System.out.println(i);
		System.out.println(++i);// preincrement --> it will increse the value in same line
		System.out.println(i++);// postIncrement --> it will increase the value in next line
		System.out.println(i);
	}
	
}
